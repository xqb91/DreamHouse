    public DetalleEmpleado(Empleado e) {
        initComponents();
        this.setLocationRelativeTo(null);
        this.setTitle("StarLabs DreamHome :: Detalle del empleado #"+e.getNumempleado());
        lblNumeroEmpleado.setText(e.getNumempleado()+"");
        lblNombreCompleto.setText(e.getNombre()+" "+e.getApaterno()+" "+e.getAmaterno());
        lblSexo.setText((e.getSexo() == 'F') ? "Femenino": "Masculino");
        lblCargo.setText(e.getCargo().getNombre());
        Date fecha = e.getFechnac();
        lblFecNac.setText(fecha.getDay()+" de "+cambiaMes(fecha.getMonth())+" de 19"+fecha.getYear());
        
        lblSucursal.setText(e.getNumoficina().getNumoficina()+": "+e.getNumoficina().getCiudad().getNombre());
        lblDireccion.setText(e.getNumoficina().getCalle()+", "+e.getNumoficina().getCiudad().getNombre());
        lblProvincia.setText(e.getNumoficina().getCiudad().getProvincia().getNombre());
        lblRegion.setText(e.getNumoficina().getCiudad().getProvincia().getRegion().getNombre());
        lblPais.setText(e.getNumoficina().getCiudad().getProvincia().getRegion().getPais().getNombre());
       
        lblSueldo.setText(((int)e.getSalario())+"");
        Calendar cal = Calendar.getInstance();
        lblComisiones.setText(cal.get(Calendar.MONTH)+" es el ultimo dia del mes, y el primero es: "+cal.getMinimum(Calendar.DAY_OF_MONTH));
    }

