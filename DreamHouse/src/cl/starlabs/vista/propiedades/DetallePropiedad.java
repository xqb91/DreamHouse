    public DetallePropiedad(Propiedad p) {
        initComponents();
        this.p = p;
        setLocationRelativeTo(null);
        emf = Persistence.createEntityManagerFactory("PU");
        lblPropiedadNro.setText(p.getNumpropiedad()+"");
        lblDireccion.setText(p.getCalle()+", "+this.buscarCiudad(p.getCiudad()).getNombre()+", "+this.buscarCiudad(p.getCiudad()).getProvincia().getRegion().getNombre());
        lblTipoPropiedad.setText(p.getTipo().getNombre());
        lblHabitaciones.setText(p.getHab()+" Habitaciones");
        lblValorArriendo.setText("$"+p.getRenta());
        if(p.getDisponible() ==  '1') {
            lblDisponible.setText("Disponible");
            lblDisponible.setForeground(Color.green);
        }else{
            lblDisponible.setForeground(Color.red);
            lblDisponible.setText("No Disponible");
        }
        lblRut.setText(p.getNumpropietario().getRut()+"-"+p.getNumpropietario().getDv());
        lblNombre.setText(p.getNumpropietario().getNombre()+" "+p.getNumpropietario().getApaterno()+" "+p.getNumpropietario().getAmaterno());
        lblDirProp.setText(p.getNumpropietario().getDireccion()+", "+p.getNumpropietario().getCiudad().getNombre()+", "+p.getNumpropietario().getCiudad().getProvincia().getRegion().getNombre());
        lblEmail.setText(p.getNumpropietario().getEmail());
    }

