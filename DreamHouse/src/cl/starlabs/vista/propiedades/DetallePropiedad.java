    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int valor = JOptionPane.showConfirmDialog(null, "驴Realmente desea eliminar esta propiedad?");
        if(valor == 0)
        {
            try {
                new PropiedadJpaController(emf).destroy(p.getNumpropiedad());
                JOptionPane.showMessageDialog(null, "Propiedad Eliminada con 茅xito");
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error: El servidor dijo..."+e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed
    public Ciudad buscarCiudad(BigInteger ciudadId){
        try {
            return new CiudadJpaController(emf).findCiudad(new BigDecimal(ciudadId));
        } catch (Exception e) {
            return null;
        }
    } 
